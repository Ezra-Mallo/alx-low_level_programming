#include "3-calc.h"


/**
 * op_add - for adding
 * @a: 1st parameter to be added
 * @b: 2nd parameter to be added
 * Return: result of the addition
 */
int op_add(int a, int b)
{
	return (a + b);
}



/**
 * op_sub - for adding
 * @a: 1st parameter to be subtracted
 * @b: 2nd parameter to be subtrated
 * Return: result of the subtration
 */
int op_sub(int a, int b)
{
	return (a - b);
}



/**
 * op_mul - for multiplication
 * @a: 1st parameter to be multiplied
 * @b: 2nd parameter to be multiplied
 * Return: result of the multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}



/**
 * op_div - for division
 * @a: 1st parameter to be numerator
 * @b: 2nd parameter to be divisor
 * Return: result of the division
 */
int op_div(int a, int b)
{
	return (a / b);
}



/**
 * op_add - for modulus division
 * @a: 1st parameter to be numerator
 * @b: 2nd parameter to be divisor
 * Return: result of the remender
 */
int op_mod(int a, int b)
{
	return (a % b);
}
